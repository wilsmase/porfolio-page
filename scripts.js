// =============================================
// COLOR MAP
// =============================================
const COLORS = {
    0: null,
    1: '#C45A0A',
    2: '#F97316',
    3: '#FFB347',
    4: '#FFD4A3',
};

// =============================================
// DRAW FUNCTION
// =============================================
const PIXEL = 20;
const spriteSize = 32 * PIXEL;
const x = (canvas.width - spriteSize) / 2;
const y = (canvas.height - spriteSize) / 2;

drawSprite('soccerBall', x, y);
function drawSprite(spriteKey, x, y) {
    const grid = SPRITES[spriteKey];

    grid.forEach((row, rowIndex) => {
        row.forEach((cell, colIndex) => {
            if (cell !== 0) {
                ctx.fillStyle = COLORS[cell];
                ctx.fillRect(
                    x + colIndex * PIXEL,
                    y + rowIndex * PIXEL,
                    PIXEL - 1,
                    PIXEL - 1
                );
            }
        });
    });
}


// =============================================
// ANIMATION STATE
// =============================================
let state = 'visible';
let currentIdx = 0;
let particles = [];
let timer = 0;
const HOLD_TIME = 2500;

// =============================================
// BUILD PARTICLES FROM SPRITE
// =============================================
function buildParticles(spriteKey) {
    const grid = SPRITES[spriteKey];
    const pts = [];

    grid.forEach((row, rowIndex) => {
        row.forEach((cell, colIndex) => {
            if (cell !== 0) {
                const px = x + colIndex * PIXEL;
                const py = y + rowIndex * PIXEL;
                pts.push({
                    x: px,
                    y: py,
                    tx: px,
                    ty: py,
                    vx: (Math.random() - 0.5) * 15,
                    vy: (Math.random() - 0.5) * 15,
                    color: COLORS[cell],
                    alpha: 1,
                });
            }
        });
    });

    return pts;
}
// =============================================
// ANIMATION LOOP
// =============================================
let lastTime = performance.now();

function animate(now) {
    const dt = now - lastTime;
    lastTime = now;

    ctx.clearRect(0, 0, canvas.width, canvas.height);

    if (state === 'visible') {
        drawSprite(SPRITE_KEYS[currentIdx], x, y);
        timer += dt;

        if (timer >= HOLD_TIME) {
            state = 'exploding';
            timer = 0;
            particles = buildParticles(SPRITE_KEYS[currentIdx]);
        }
    }

    if (state === 'exploding') {
        let allGone = true;

        particles.forEach((p) => {
            p.x += p.vx;
            p.y += p.vy;
            p.vy += 0.4;
            p.alpha -= 0.018;

            if (p.alpha > 0) {
                allGone = false;
                ctx.globalAlpha = p.alpha;
                ctx.fillStyle = p.color;
                ctx.fillRect(p.x, p.y, PIXEL - 1, PIXEL - 1);
            }
        });

        ctx.globalAlpha = 1;

        if (allGone) {
            currentIdx = (currentIdx + 1) % SPRITE_KEYS.length;
            state = 'assembling';
            particles = buildParticles(SPRITE_KEYS[currentIdx]);

            particles.forEach((p) => {
                p.x = Math.random() * canvas.width;
                p.y = Math.random() * canvas.height;
                p.alpha = 0;
            });
        }
    }

    if (state === 'assembling') {
        let allArrived = true;

        particles.forEach((p) => {
            p.x += (p.tx - p.x) * 0.08;
            p.y += (p.ty - p.y) * 0.08;
            p.alpha = Math.min(1, p.alpha + 0.03);

            const dist = Math.hypot(p.tx - p.x, p.ty - p.y);
            if (dist > 1) allArrived = false;

            ctx.globalAlpha = p.alpha;
            ctx.fillStyle = p.color;
            ctx.fillRect(p.x, p.y, PIXEL - 1, PIXEL - 1);
        });

        ctx.globalAlpha = 1;

        if (allArrived) {
            state = 'visible';
            timer = 0;
        }
    }

    requestAnimationFrame(animate);
}

requestAnimationFrame(animate);
